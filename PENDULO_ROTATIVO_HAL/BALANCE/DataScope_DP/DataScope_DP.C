unsigned char DataScope_OutPut_Buffer[42] = {0};	   //Buffer de envio da porta serial


//Descricao da funcao: Converte dados de ponto flutuante de precisao simples em dados de 4 bytes e armazena-os no endereco especificado
//Nota adicional: os usuarios nao precisam operar esta funcao diretamente
//target: alvo de dados de precisao unica
//buf: array a ser escrito
//beg: Especifique de qual elemento do array comecar a escrever
//Funcao nao tem retorno
void Float2Byte(float *target,unsigned char *buf,unsigned char beg)
{
    unsigned char *point;
    point = (unsigned char*)target;	  //Obtenha o endereco do float
    buf[beg]   = point[0];
    buf[beg+1] = point[1];
    buf[beg+2] = point[2];
    buf[beg+3] = point[3];
}
 
 
//Descricao da funcao: escreve os dados de ponto flutuante de precisao simples do canal a serem enviados para o buffer de envio
//Dados: dados do canal
//Canal: selecione o canal (1-10)
//Funcao nao tem retorno 
void DataScope_Get_Channel_Data(float Data,unsigned char Channel)
{
	if ( (Channel > 10) || (Channel == 0) ) return;  //Se o numero de canais for maior que 10 ou igual a 0, saltara diretamente sem executar a funcao.
  else
  {
     switch (Channel)
		{
      case 1:  Float2Byte(&Data,DataScope_OutPut_Buffer,1); break;
      case 2:  Float2Byte(&Data,DataScope_OutPut_Buffer,5); break;
		  case 3:  Float2Byte(&Data,DataScope_OutPut_Buffer,9); break;
		  case 4:  Float2Byte(&Data,DataScope_OutPut_Buffer,13); break;
		  case 5:  Float2Byte(&Data,DataScope_OutPut_Buffer,17); break;
		  case 6:  Float2Byte(&Data,DataScope_OutPut_Buffer,21); break;
		  case 7:  Float2Byte(&Data,DataScope_OutPut_Buffer,25); break;
		  case 8:  Float2Byte(&Data,DataScope_OutPut_Buffer,29); break;
		  case 9:  Float2Byte(&Data,DataScope_OutPut_Buffer,33); break;
		  case 10: Float2Byte(&Data,DataScope_OutPut_Buffer,37); break;
		}
  }	 
}


//Descricao da funcao: Gera um formato de quadro que o DataScopeV1.0 possa reconhecer corretamente
//Channel_Number, o numero de canais a serem enviados
